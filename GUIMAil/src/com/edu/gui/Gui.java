    public HashMap<String, Integer> convertList(ArrayList<String> letter) {
        HashMap<String, Integer> map = new HashMap<>();
        for (int i = 0; i < letter.size(); i++) {
            if (map.get(letter.get(i)) == null) {
                map.put(letter.get(i), 1);
            } else {
                map.put(letter.get(i), map.get(letter.get(i)) + 1);
            }
        }
        return map;
    }

