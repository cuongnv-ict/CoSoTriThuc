    public void loadTable(ArrayList<String> letter, ArrayList<String> stopwords) {
        Object[] nameCol = {"STT", "Từ thường", "Tần số", "Stemming", "Tần số", "StopWords", "Tần số"};
        HashMap<String, Integer> m_letter = convertList(letter);
        ArrayList<String> a_letter = getKey(m_letter);
        HashMap<String, Integer> m_stemming = convertList(Stemmer.StemArrayList(letter));
        ArrayList<String> a_stemming = getKey(m_stemming);
        HashMap<String, Integer> m_stopwords = convertList(stopwords);
        ArrayList<String> a_stopwords = getKey(m_stopwords);
        int max = Math.max(m_letter.size(), m_stopwords.size());
        Object[][] rowColumn = new Object[max][];
        for (int i = 0; i < max; i++) {
            Object a[] = new Object[7];
            a[0] = i + 1;
            if (i < a_letter.size()) {
                a[1] = a_letter.get(i);
                a[2] = m_letter.get(a_letter.get(i));
            } else {
                a[1] = "";
                a[2] = "";
            }
            if (i < a_stemming.size()) {
                a[3] = a_stemming.get(i);
                a[4] = m_stemming.get(a_stemming.get(i));
            } else {
                a[3] = "";
                a[4] = "";
            }
            if (i < a_stopwords.size()) {
                a[5] = a_stopwords.get(i);
                a[6] = m_stopwords.get(a_stopwords.get(i));
            } else {
                a[5] = "";
                a[6] = "";
            }
            rowColumn[i] = a;
        }
        DefaultTableModel model = new DefaultTableModel(rowColumn, nameCol) {
            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return false;
            }
        };
        tbl_mail.setModel(model);
    }
